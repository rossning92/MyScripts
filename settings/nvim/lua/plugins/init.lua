        version = '1.*',
        --- @module 'blink.cmp'
        --- @type blink.cmp.Config
        opts = {
            sources = {
                default = { 'lsp', 'path', 'snippets', 'buffer' },
            },
            keymap = { preset = 'super-tab' }, -- tab to accept
        },
    },

